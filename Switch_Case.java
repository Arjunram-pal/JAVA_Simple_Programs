public class Switch_Case 
{
    public static void  main(String[] args)
    {
        int i=2;
        switch(i)
        {
            case 1:
            {
                System.out.println("Nunmber is ONE.");
                break;
            }
            case 2:
            {
                System.out.println("Nunmber is TWO.");
                break;
            }
            default:
            {
              System.out.println("Invalid number!!!");
              break;
            }
        }
    }
    
}
